def manual_return_rate_check() -> None:
    df = pd.read_csv(CLEAN_DATA, low_memory=False)
    df["is_returned"] = pd.to_numeric(df["is_returned"], errors="coerce").fillna(0).astype(int)
    df["order_date"] = pd.to_datetime(df["order_date"], errors="coerce")
    grouped = (
        df.groupby("customer_id", dropna=False)
        .agg(
            customer_name=("customer_name", "first"),
            total_orders=("order_id", "count"),
            return_count=("is_returned", "sum"),
            first_order_date=("order_date", "min"),
            last_order_date=("order_date", "max"),
        )
        .reset_index()
    )
    grouped["no_return_count"] = grouped["total_orders"] - grouped["return_count"]
    grouped["manual_return_rate"] = grouped["return_count"] / grouped["total_orders"]
    grouped["manual_return_rate_pct"] = grouped["manual_return_rate"] * 100

    mixed = grouped[(grouped["total_orders"] >= 2) & (grouped["return_count"] > 0) & (grouped["no_return_count"] > 0)]
    if len(mixed) >= 10:
        sample = mixed.sample(10, random_state=42)
    else:
        sample = grouped[grouped["total_orders"] >= 2].sample(min(10, len(grouped)), random_state=42)
    sample = sample.sort_values(["total_orders", "return_count", "customer_id"], ascending=[False, False, True]).copy()

    detail_rows = []
    summary_rows = []
    for _, row in sample.iterrows():
        customer_id = row["customer_id"]
        orders = df[df["customer_id"].eq(customer_id)].sort_values(["order_date", "order_id"]).copy()
        order_ids = orders["order_id"].astype(str).tolist()
        returned_order_ids = orders.loc[orders["is_returned"].eq(1), "order_id"].astype(str).tolist()
        no_return_order_ids = orders.loc[orders["is_returned"].eq(0), "order_id"].astype(str).tolist()
        summary_rows.append(
            {
                "customer_id": customer_id,
                "customer_name": row["customer_name"],
                "total_orders": int(row["total_orders"]),
                "return_count": int(row["return_count"]),
                "no_return_count": int(row["no_return_count"]),
                "manual_formula": f"{int(row['return_count'])}/{int(row['total_orders'])}",
                "manual_return_rate": round(float(row["manual_return_rate"]), 6),
                "manual_return_rate_pct": round(float(row["manual_return_rate_pct"]), 2),
                "first_order_date": row["first_order_date"],
                "last_order_date": row["last_order_date"],
                "sample_order_ids": ", ".join(order_ids[:12]),
                "returned_order_ids": ", ".join(returned_order_ids[:12]),
                "no_return_order_ids": ", ".join(no_return_order_ids[:12]),
                "excel_check_note": "return_count / total_orders เช่น 4/8 = 0.5 = 50%",
            }
        )
        prior_orders = 0
        prior_returns = 0
        for _, order in orders.iterrows():
            calc_rate = prior_returns / prior_orders if prior_orders else 0.0
            dataset_hist_count = pd.to_numeric(pd.Series([order.get("hist_order_count", np.nan)]), errors="coerce").iloc[0]
            dataset_hist_rate = pd.to_numeric(pd.Series([order.get("hist_return_rate", np.nan)]), errors="coerce").iloc[0]
            detail_rows.append(
                {
                    "customer_id": customer_id,
                    "order_id": order["order_id"],
                    "order_date": order["order_date"],
                    "current_order_is_returned": int(order["is_returned"]),
                    "manual_prior_order_count": prior_orders,
                    "manual_prior_return_count": prior_returns,
                    "manual_prior_return_rate": round(calc_rate, 6),
                    "dataset_hist_order_count": dataset_hist_count,
                    "dataset_hist_return_rate": dataset_hist_rate,
                    "hist_count_match": bool(prior_orders == dataset_hist_count) if not pd.isna(dataset_hist_count) else "",
                    "hist_rate_abs_diff": round(abs(calc_rate - dataset_hist_rate), 8) if not pd.isna(dataset_hist_rate) else "",
                    "formula_note": f"{prior_returns}/{prior_orders} = {calc_rate:.4f}" if prior_orders else "0 prior orders => 0",
                }
            )
            prior_orders += 1
            prior_returns += int(order["is_returned"])

    pd.DataFrame(summary_rows).to_csv(REPORT_AUDIT_DIR / "customer_return_rate_manual_check.csv", index=False, encoding="utf-8-sig")
    pd.DataFrame(detail_rows).to_csv(REPORT_AUDIT_DIR / "customer_return_rate_order_level_check.csv", index=False, encoding="utf-8-sig")


def load_feature_names_from_joblib(path: Path) -> list[str]:
    if not path.exists():
        return []
    data = joblib.load(path)
    names = data.get("feature_names", [])
    return list(names)


