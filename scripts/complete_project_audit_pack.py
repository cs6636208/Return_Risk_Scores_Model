def feature_risk_type(col: str, leakage_cols: set[str], post_delivery_cols: set[str], id_cols: set[str]) -> str:
    if col == "is_returned":
        return "target"
    if col in leakage_cols:
        return "leakage/post-return"
    if col in post_delivery_cols:
        return "post-delivery"
    if col in id_cols:
        return "identifier/query-only"
    return "usable_or_engineered"


def feature_group(col: str) -> str:
    patterns = [
        ("customer_history", ["hist_", "cust_", "days_since_last", "return_ratio", "orders_before", "returns_before"]),
        ("category_product", ["category", "brand", "product", "fragile", "rating"]),
        ("payment_channel", ["payment", "channel", "cod"]),
        ("promotion_discount", ["promo", "discount", "campaign", "tier_discount"]),
        ("logistics", ["courier", "delivery", "delay", "damage", "logistics"]),
        ("location_profile", ["province", "gender", "age", "membership", "tenure"]),
        ("time", ["hour", "month", "dayofweek", "weekend", "date"]),
        ("amount_price", ["price", "amount", "quantity", "spend"]),
        ("target_or_return", ["return", "refund", "risk", "shap", "score"]),
    ]
    low = col.lower()
    for group, keys in patterns:
        if any(key in low for key in keys):
            return group
    return "other"


def evaluate_v1_model() -> dict[str, Any] | None:
    model_path = ROOT / "docs" / "version 1" / "model" / "best_model.pkl"
    data_path = ROOT / "docs" / "version 1" / "data" / "features" / "train_test_sets.pkl"
    if not model_path.exists() or not data_path.exists():
        return None
    model = joblib.load(model_path)
    data = joblib.load(data_path)
    x_test = data["X_test"]
    y_test = data["y_test"]
    proba = model.predict_proba(x_test)[:, 1]
    pred = (proba >= 0.5).astype(int)
    tn, fp, fn, tp = confusion_matrix(y_test, pred, labels=[0, 1]).ravel()
    return {
        "version": "V1",
        "model": type(model).__name__,
        "threshold": 0.5,
        "accuracy": accuracy_score(y_test, pred),
        "recall": recall_score(y_test, pred, zero_division=0),
        "precision": precision_score(y_test, pred, zero_division=0),
        "f1": f1_score(y_test, pred, zero_division=0),
        "auc": roc_auc_score(y_test, proba),
        "avg_precision": average_precision_score(y_test, proba),
        "cost": fn * 150 + fp * 50,
        "tn": int(tn),
        "fp": int(fp),
        "fn": int(fn),
        "tp": int(tp),
        "rating": rating(accuracy_score(y_test, pred), recall_score(y_test, pred, zero_division=0), f1_score(y_test, pred, zero_division=0), fn * 150 + fp * 50),
        "note": "computed from packaged V1 model and test set",
    }


def rating(acc: float, recall: float, f1: float, cost: float) -> str:
    if acc >= 0.80 and f1 >= 0.60 and recall >= 0.60 and cost <= 20000:
        return "A"
    if acc >= 0.68 and f1 >= 0.52 and recall >= 0.60 and cost <= 28000:
        return "B+"
    if acc >= 0.65 and f1 >= 0.48 and recall >= 0.55:
        return "B"
    if acc >= 0.60 and f1 >= 0.42:
        return "C"
    return "D"


