def copy_business_charts() -> list[dict[str, Any]]:
    rows = []
    for idx, insight in enumerate(BUSINESS_INSIGHTS, start=1):
        chart_exists = insight.chart.exists()
        target = ""
        if chart_exists:
            suffix = insight.chart.suffix.lower()
            target_path = BUSINESS_CHART_DIR / f"{idx:02d}_{insight.name.lower().replace(' ', '_')}{suffix}"
            shutil.copy2(insight.chart, target_path)
            target = str(target_path.relative_to(ROOT))
        rows.append(
            {
                "no": idx,
                "insight": insight.name,
                "source_chart": str(insight.chart.relative_to(ROOT)) if insight.chart.exists() else str(insight.chart),
                "copied_chart": target,
                "chart_found": chart_exists,
                "business_insight": insight.insight,
                "reason_selected": insight.reason,
                "mapped_features": ", ".join(insight.features),
                "model_or_business_action": insight.action,
            }
        )
    return rows


